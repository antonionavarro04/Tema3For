package ejercicios;

public class Ejercicio2 {
    public static void main(String[] args) {
        // ? Programa que con un bucle for muestre los números pares desde el 1 al 200
        // ^ Declaramos la variable i (incremento)
        short i;

        // ! Pequeña descripción del programa
        System.out.println("Programa que con un bucle for muestre los número pares desde el 1 al 200");

        // ? Salto de Línea Triple
        System.out.println("\n" + "-------------------------------" + "\n");

        // ! Bucle for que muestra los números pares desde el 1 al 200
        for (i = 2; i <= 200; i += 2) {
            System.out.println(i);
        }

        // € Hecho por Antonio Navarro
    }
}
